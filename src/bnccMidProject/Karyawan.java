package bnccMidProject;

public class Karyawan {
	
	String ID;
	String nama;
	String gender;
	String jabatan;
	float gajiKaryawan;
	

	public Karyawan(String iD, String nama, String gender, String jabatan, float gajiKaryawan) {
		super();
		ID = iD;
		this.nama = nama;
		this.gender = gender;
		this.jabatan = jabatan;
		this.gajiKaryawan = gajiKaryawan;
	}

}
